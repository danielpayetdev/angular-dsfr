type IconsKey = keyof typeof IconsData;
export type Icons = typeof IconsData[IconsKey];
export type IconStyle = "fill" | "line";

export class Icon {
    public static get(iconName: Icons, style: IconStyle = "fill") {
        if (withoutstyle.includes(iconName as any)) {
            return `fr-icon-${iconName}`;
        } else {
            const iconStyle = Icon.getStyle(iconName, style);
            return `fr-icon-${iconName}-${iconStyle}`;
        }
    }

    private static getStyle(iconName: Icons, style: IconStyle): IconStyle | "line-double" {
        if (onlyWithFillStyle.includes(iconName as any) && style === "line") {
            return (style = "fill");
        } else if (onlyWithLineStyle.includes(iconName as any) && style === "fill") {
            return (style = "line");
        }
        if (style === "line" && withLineDoubleStyle.includes(iconName as any)) {
            return "line-double";
        } else {
            return style;
        }
    }
}
